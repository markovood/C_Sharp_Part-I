using System;

namespace Problem1.OddOrEvenIntegers
{
    class OddOrEvenIntegers
    {
        static void Main()
        {
            // Write an expression that checks if given integer is odd or even.

            int n = 0;

            if (n % 2 == 0)
            {
                Console.WriteLine("int n is EVEN. ");
            }

            else
            {
                Console.WriteLine("int n is ODD. ");
            }
        }
    }
}
